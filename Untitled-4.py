a = "5Hel4e02"  # Define a string
for i in a:  # Loop through each character in the string
    if i.isdigit():  # Check if the character is a digit
        print(i, "is digit")  # Print the character and "is digit" if it's a digit
    else:  # If the character is not a digit
        print(i, "is letter")  # Print the character and "is letter" if it's not a digit