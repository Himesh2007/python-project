# Define a function to add two numbers
def add(x, y):
    return x + y  # Return the sum of x and y

# Define a function to subtract the second number from the first number
def subtract(x, y):
    return x - y  # Return the result of x minus y

# Define a function to multiply two numbers
def multiply(x, y):
    return x * y  # Return the product of x and y

# Define a function to divide the first number by the second number
def divide(x, y):
    return x / y  # Return the result of x divided by y